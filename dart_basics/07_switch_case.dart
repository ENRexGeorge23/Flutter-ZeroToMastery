void main() {
  String name = 'EN Rex';

  switch (name) {
    case 'Hans':
      print('My name is  Hans');
      break;
    case "Peter":
      print('My name is Peter');
      break;
    case 'EN Rex':
      print('My name is EN Rex');
      break;
    default:
      print('I do not know my name');
  }
}
